let color = prompt("Mời bạn nhập màu")
switch (color) {
    case 1:
        document.write("màu xanh")
        break;
    case 2:
        document.write("màu đỏ")
        break;
    case 3:
        document.write("màu vàng")
        break;
    default:
        document.write("Không hợp lệ");
        break;
}